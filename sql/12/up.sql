CREATE TABLE Ingest (
  id BIGINT NOT NULL AUTO_INCREMENT,
  created DATETIME NOT NULL,
  updated DATETIME NOT NULL,
  active BIT NOT NULL,
  zip VARCHAR(10),
  country_code CHAR(2),
  term VARCHAR(250),
  distance INTEGER,
  CONSTRAINT pk_ingest PRIMARY KEY (id),
  CONSTRAINT unq_ingest UNIQUE(zip, country_code, term)
) ENGINE = InnoDB;

CREATE TABLE Category (
  id BIGINT NOT NULL AUTO_INCREMENT,
  name VARCHAR(255),
  CONSTRAINT pk_category PRIMARY KEY (id),
  CONSTRAINT unq_category_name UNIQUE(name)
) ENGINE = InnoDB;

CREATE TABLE Location (
  id BIGINT NOT NULL AUTO_INCREMENT,
  active BIT NOT NULL,  
  external_id VARCHAR(255),
  name VARCHAR(255),
  category VARCHAR(255),
  street VARCHAR(255),
  city VARCHAR(50),
  state CHAR(2),
  zip VARCHAR(10),
  country_code CHAR(2) NOT NULL,
  phone VARCHAR(20),
  latitude DOUBLE,
  longitude DOUBLE,
  url VARCHAR(500),
  source INTEGER NOT NULL,
  CONSTRAINT pk_location PRIMARY KEY (id),
  CONSTRAINT unq_location UNIQUE(external_id, source)  
) ENGINE = InnoDB;

CREATE TABLE Location_Category (
  id BIGINT NOT NULL AUTO_INCREMENT,
  location_id BIGINT NOT NULL,
  category_id BIGINT NOT NULL,
  CONSTRAINT pk_location_category PRIMARY KEY (id),
  CONSTRAINT fk_lc_location FOREIGN KEY(location_id) REFERENCES Location(id),
  CONSTRAINT fk_lc_category FOREIGN KEY(category_id) REFERENCES Category(id)
) ENGINE = InnoDB;

CREATE TABLE Location_Ingest (
  id BIGINT NOT NULL AUTO_INCREMENT,
  location_id BIGINT NOT NULL,
  ingest_id BIGINT NOT NULL,
  CONSTRAINT pk_location_ingest PRIMARY KEY (id),
  CONSTRAINT fk_li_location FOREIGN KEY(location_id) REFERENCES Location(id),
  CONSTRAINT fk_li_ingest FOREIGN KEY(ingest_id) REFERENCES Ingest(id)
) ENGINE = InnoDB;


